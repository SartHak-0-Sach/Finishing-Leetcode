using namespace std;

class Solution
{
public:
    int reverse(int x)
    {
        int ans = 0;
        bool negativeNum = false;

        if (x < 0)
        {
            negativeNum = true;
            x = abs(x);
        }

        while (x > 0)
        {
            int lastDigit = x % 10;

            // Check for overflow and underflow
            if (ans > INT_MAX / 10 || (ans == INT_MAX / 10 && lastDigit > 7))
            {
                return 0;
            }
            if (-1 * ans < INT_MIN / 10 || (-1 * ans == INT_MIN / 10 && lastDigit < -8))
            {
                return 0;
            }

            ans = ans * 10 + lastDigit;
            x /= 10;
        }

        if (negativeNum)
        {
            return -1 * ans;
        }

        return ans;
    }
};